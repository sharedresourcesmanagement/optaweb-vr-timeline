package org.optaweb.vehiclerouting.integration.spring;

import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.ViewControllerRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

@Configuration
public class WebConfig implements WebMvcConfigurer {

    @Override
    public void addViewControllers(ViewControllerRegistry registry) {
        // Forward requests without file extensions (e.g. /vehicles, but not /assets/images/optaplanner.png)
        // to /index.html.
        // In this case "page" is a variable name that is not used but is necessary to enable regexp matching.
        registry
                .addViewController("/{page:\\w+}")
                .setViewName("forward:/");
    }
}
